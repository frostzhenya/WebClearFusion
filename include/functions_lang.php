	$date = date('d-m-Y [H:i:s]');

	function getLocale($locale)
		{
  			switch ($locale):
      			case 0:
      			$locale = "English";
      			break;
  
    			case 1:
      			$locale = "Korean";
      			break;
    
    			case 2:
      			$locale = "French";
      			break;

    			case 3:
      			$locale = "German";
      			break;
      
    			case 4:
      			$locale = "Chinese";
      			break;
      
    			case 5:
      			$locale = "Taiwanese";
     			break;
      
    			case 6:
      			$locale = "Spanish";
      			break;
      
    			case 7:
      			$locale = "Spanish Mexico";
      			break;
      
    			case 8:
      			$locale = "Русский";
      			break;

  			endswitch;

			return $locale;
  
		}

	//=============================================================================================
	// функция, создающая switch языков,то есть поддержака нескольких языков, при это не 
	// переключаясь в config настройках
	if (isset($_SESSION['lang']))
		{
    			switch($_SESSION['lang'])
    			{
        			case "russian":
        			$config['lang'] = "russian";
        			break;
        			case "english":
        			$config['lang'] = "english";
        			break;
        			default:
        			unset($_SESSION['lang']);
       	 			break;
    			}
		}
